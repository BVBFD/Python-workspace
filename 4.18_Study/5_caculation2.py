number = 2 + 3 * 4 # 14
print(number)

number = number + 2 # 16
print(number)

number += 2 # number = number + 2 와 동일
print(number) # 18

number *= 2 # number = number * 2 와 동일
print(number) # 36

number /= 2 # number = number / 2 와 동일
print(number) # 18

number -= 2 # number = number - 2 와 동일
print(number) # 16

number %= 2 # number = number % 2 와 동일
print(number) # 0